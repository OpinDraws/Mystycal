# Импортируем "движок" языка (файл mystical.py должен лежать рядом!)
from mystical import Sentence

# --- НАСТРОЙКА ЗАКЛИНАНИЯ ---

# Мы создаем "Предложение" (Sentence).
# В языке Mystical это визуальная структура: Глагол (центр) + Объекты (вокруг).

# verb = Действие (Глагол)
# objects = На что направлено действие (Список объектов)

spell = Sentence(
    verb="Create",      # Что сделать? -> Создать
    objects=["Light"]   # Что именно? -> Свет
)

# --- ГЕНЕРАЦИЯ КОДА (PostScript) ---

# 1. Обязательный заголовок для файла .ps
print("%!PS")

# 2. Настройка холста
# Переносим "перо" в центр листа (300 точек вправо, 400 вверх),
# иначе круг нарисуется в углу и будет обрезан.
print("300 400 translate")

# Увеличиваем масштаб в 3 раза, чтобы круг был большим
print("3 3 scale")

# 3. Рисуем само заклинание
spell.draw()

# 4. Команда "показать страницу" (завершает файл)
print("showpage")